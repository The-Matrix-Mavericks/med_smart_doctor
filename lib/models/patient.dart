class Patient {
  final String name;
  final String gender;
  final String age;
  final String profession;
  final String imageUrl;
  final String date;
  final String time;

  const Patient({
    required this.name,
    required this.gender,
    required this.age,
    required this.profession,
    required this.imageUrl,
    required this.date,
    required this.time,
  });
}
